package com.example.test.domain.response;

import java.time.Instant;
import java.util.List;

public class ResponsePaginationCompanyDTO {
    private Long id;

    private String name, address, logo, description;

    private Instant createdAt, updatedAt;

    public static class UserFollowed {
        private Long id;
        private String email, name;

        public Long getId() {
            return id;
        }

        public void setId(Long id) {
            this.id = id;
        }

        public String getEmail() {
            return email;
        }

        public void setEmail(String email) {
            this.email = email;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

    }

    private List<UserFollowed> usersFollowed;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Instant getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Instant createdAt) {
        this.createdAt = createdAt;
    }

    public Instant getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Instant updatedAt) {
        this.updatedAt = updatedAt;
    }

    public List<UserFollowed> getUsersFollowed() {
        return usersFollowed;
    }

    public void setUsersFollowed(List<UserFollowed> usersFollowed) {
        this.usersFollowed = usersFollowed;
    }

}
